class Product {
  final String name;
  final String id;
  final String description;
  final String place;
  final String type;
  final int restCount;
  final int reservedCount;
  final int allCount;
  final double price;
  final String image;

  Product(this.name, this.id, this.description, this.place, this.type,
      this.restCount, this.reservedCount, this.allCount, this.price, this.image);
}
